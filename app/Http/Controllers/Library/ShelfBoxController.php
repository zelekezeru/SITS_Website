<?php

namespace App\Http\Controllers\Library;

use App\Http\Controllers\Controller;

use App\Models\Row;
use App\Models\ShelfBox;
use Illuminate\Http\RedirectResponse;
use Illuminate\Http\Request;
use Illuminate\Http\Response as HttpResponse;
use SimpleSoftwareIO\QrCode\Facades\QrCode;

class ShelfBoxController extends Controller
{
    public function store(Request $request, Row $row): RedirectResponse
    {
        $validated = $request->validate([
            'label'    => 'required|string|max:20',
            'capacity' => 'nullable|integer|min:1|max:65535',
        ]);

        $row->shelfBoxes()->create($validated);
        // tracking_hash auto-generated by HasTrackingHash trait

        return redirect()->route('campuses.show', $row->floor->campus_id)
            ->with('success', 'Shelf box added.');
    }

    public function update(Request $request, ShelfBox $shelfBox): RedirectResponse
    {
        $validated = $request->validate([
            'label'    => 'required|string|max:20',
            'capacity' => 'nullable|integer|min:1|max:65535',
        ]);

        $shelfBox->update($validated);
        // tracking_hash is immutable — never regenerated after creation

        return redirect()->route('campuses.show', $shelfBox->campus()?->id)
            ->with('success', 'Shelf box updated.');
    }

    public function destroy(ShelfBox $shelfBox): RedirectResponse
    {
        $campusId = $shelfBox->campus()?->id;
        $shelfBox->delete();

        return redirect()->route('campuses.show', $campusId)
            ->with('success', 'Shelf box archived.');
    }

    /**
     * Return a QR code PNG for the shelf box's tracking_hash.
     * Works for physical label printing now; hash is RFID-compatible later.
     */
    public function qr(ShelfBox $shelfBox): HttpResponse
    {
        $png = QrCode::format('png')
            ->size(300)
            ->errorCorrection('M')
            ->generate($shelfBox->tracking_hash);

        return response($png, 200, [
            'Content-Type'        => 'image/png',
            'Content-Disposition' => 'inline; filename="shelf-'.$shelfBox->label.'.png"',
        ]);
    }
}
